    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
       Livro livro = new Livro();
       DefaultTableModel tabelaLivros = (DefaultTableModel) jTabelaLivros.getModel();
       
       for(int i=0; i<biblioteca.getLivros().size(); i++){
            livro = biblioteca.getLivros().get(i);
           String[] produtosArray = {livro.getId(), livro.getTitulo(), String.valueOf(livro.getQuantidade())};   
           tabelaLivros.addRow(produtosArray);
       }
    }//GEN-LAST:event_formWindowOpened

